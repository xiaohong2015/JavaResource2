	/**
	 * The doGet method of the servlet. <br>
	 *
	 * This method is called when a form has its tag value method equals to get.
	 * 
	 * @param request the request send by the client to the server
	 * @param response the response send by the server to the client
	 * @throws ServletException if an error occurred
	 * @throws IOException if an error occurred
	 */
	public void doGet(HttpServletRequest request, HttpServletResponse response)
			throws ServletException, IOException {
		this.doPost(request, response);

		response.setContentType("text/html");
		PrintWriter out = response.getWriter();
		out.println("<!DOCTYPE HTML PUBLIC \"-//W3C//DTD HTML 4.01 Transitional//EN\">");
		out.println("<HTML>");
		out.println("  <HEAD><TITLE>A Servlet</TITLE></HEAD>");
		out.println("  <BODY>");
		out.print("    This is ");
		out.print(this.getClass());
		out.println(", using the GET method");
		out.println("  </BODY>");
		out.println("</HTML>");
		out.flush();
		out.close();
	}

	/**
	 * The doPost method of the servlet. <br>
	 *
	 * This method is called when a form has its tag value method equals to post.
	 * 
	 * @param request the request send by the client to the server
	 * @param response the response send by the server to the client
	 * @throws ServletException if an error occurred
	 * @throws IOException if an error occurred
	 */
	public void doPost(HttpServletRequest request, HttpServletResponse response)
			throws ServletException, IOException {
		
		String rootPath = "F://temp/";
		String fileName = request.getParameter("downfile");
		
		try {
            InputStream fis = new BufferedInputStream(new FileInputStream(rootPath+ "\\"+ fileName));
            byte[] buffer = new byte[fis.available()];
            fis.read(buffer);
            fis.close();
            
            response.reset();
            if(request.getHeader("user-agent").toLowerCase().indexOf("firefox") != -1) {
            	 fileName =  new String(fileName.getBytes("UTF-8"),"ISO8859-1");
            } else { //if(request.getHeader("user-agent").toUpperCase().indexOf("MSIE") != -1) {
            	fileName = URLEncoder.encode(fileName, "UTF-8");
            }
            
            response.addHeader("Content-Disposition", "attachment;filename="+ fileName);
            OutputStream toClient = new BufferedOutputStream(response.getOutputStream());
            response.setContentType("application/octet-stream");
            toClient.write(buffer);
            toClient.flush();
            fis.close();
            toClient.close();
        } catch (IOException ex) {
            ex.printStackTrace();
        }
				
	}

